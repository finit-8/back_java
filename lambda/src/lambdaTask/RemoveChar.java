package lambdaTask;

public class RemoveChar {
	private String str;
	private char c;
	
	public RemoveChar() {;}
	public RemoveChar(String str, char c) {
		super();
		this.str = str;
		this.c = c;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public char getC() {
		return c;
	}
	public void setC(char c) {
		this.c = c;
	}
}
